static int procsleep(PROGINFO *pip)
{
	LOCINFO		*lip = pip->lip ;
	int		rs = SR_OK ;

#if	CF_DEBUG
	if (DEBUGLEVEL(4))
	    debugprintf("b_rest/procsleep: ent\n") ;
#endif

	if (pip->debuglevel > 0) {
	    shio_printf(pip->efp,"%s: interval=%u\n",
	        pip->progname,lip->sum) ;
	}

	if (lip->sum > 0) {
	    time_t	dt = time(NULL) ;
	    time_t	waketime ;
	    uint	mv ;
	    int		i = 0 ;
	    waketime = (dt + lip->sum) ;
	    while ((rs >= 0) && (dt < waketime)) {

	        mv = MIN((waketime - dt),2) ;
#if	CF_DEBUG
	        if (DEBUGLEVEL(4))
	            debugprintf("b_rest/procsleep: msleep %u\n",mv) ;
#endif
	        rs = msleep(mv*1000) ;
#if	CF_DEBUG
	        if (DEBUGLEVEL(4))
	            debugprintf("b_rest/procsleep: msleep() rs=%d\n",rs) ;
#endif

	        i += 1 ;
	        dt = time(NULL) ;

	        if (rs >= 0) rs = lib_sigterm() ;
	        if (rs >= 0) rs = lib_sigintr() ;
	    } /* end while (resting - looping) */
	} /* end if (positive) */

#if	CF_DEBUG
	if (DEBUGLEVEL(4))
	    debugprintf("b_rest/procsleep: ret rs=%d\n",rs) ;
#endif

	return rs ;
}
/* end subroutine (procsleep) */



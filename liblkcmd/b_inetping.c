static int procargs(PROGINFO *pip,ARGINFO *aip,BITS *bop,PARAMOPT *pop,
		cchar *ofn,cchar *afn)
{
	LOCINFO		*lip = pip->lip ;
	SHIO		ofile, *ofp = &ofile ;
	int		rs ;
	int		rs1 ;
	int		c = 0 ;
	cchar		*pn = pip->progname ;
	cchar		*fmt ;

	if ((ofn == NULL) || (ofn[0] == '\0') || (ofn[0] == '-'))
	    ofn = BFILE_STDOUT ;

	if ((rs = procoutopen(pip,ofp,ofn)) >= 0) {
	    int		pan = 0 ;
	    int		cl ;
	    cchar	*cp ;

	    if (rs >= 0) {
	        int	ai ;
	        int	f ;
	        cchar	**argv = aip->argv ;
	        for (ai = 1 ; ai < aip->argc ; ai += 1) {

	            f = (ai <= aip->ai_max) && (bits_test(bop,ai) > 0) ;
	            f = f || ((ai > aip->ai_pos) && (argv[ai] != NULL)) ;
	            if (f) {
	                cp = argv[ai] ;
	                if (cp[0] != '\0') {
	                    pan += 1 ;
	                    rs = prochost(pip,pop,ofp,cp,-1) ;
	                    c += 1 ;
	                }
	            }

	            if ((rs < 0) && (! lip->f.scan)) break ;
	        } /* end for (looping through requested circuits) */
	    } /* end if (ok) */

/* process any files in the argument filename list file */

	    if ((rs >= 0) && (afn != NULL) && (afn[0] != '\0')) {
	        SHIO	afile, *afp = &afile ;

	        if (strcmp(afn,"-") == 0) afn = BFILE_STDIN ;

	        if ((rs = shio_open(afp,afn,"r",0666)) >= 0) {
	            const int	llen = LINEBUFLEN ;
	            int		len ;
	            char	lbuf[LINEBUFLEN + 1] ;

	            while ((rs = shio_readline(afp,lbuf,llen)) > 0) {
	                len = rs ;

	                if (lbuf[len - 1] == '\n') len -= 1 ;
	                lbuf[len] = '\0' ;

	                if ((cl = sfskipwhite(lbuf,len,&cp)) > 0) {
	                    if (cp[0] != '#') {
	                        pan += 1 ;
	                        rs = prochosts(pip,pop,ofp,cp,cl) ;
	                        c += rs ;
	                    }
	                }

	                if ((rs < 0) && (! lip->f.scan)) break ;
	            } /* end while (reading lines) */

	            rs1 = shio_close(afp) ;
	            if (rs >= 0) rs = rs1 ;
	        } else {
	            if (! pip->f.quiet) {
	                fmt = "%s: inaccessible argument-list (%d)\n",
	                shio_printf(pip->efp,fmt,pn,rs) ;
	                shio_printf(pip->efp,"%s: afile=%s\n",pn,afn) ;
	            }
	        } /* end if */

	    } /* end if (processing file argument file list) */

	    if ((rs >= 0) && (pan == 0) && (afn == NULL)) {
	        fmt = "%s: no hosts specified\n" ;
	        shio_printf(pip->efp,fmt,pn) ;
	    }

	    rs1 = procoutclose(pip,ofp) ;
	    if (rs >= 0) rs = rs1 ;
	} else {
	    fmt = "%s: output unavailable (%d)\n" ;
	    shio_printf(pip->efp,fmt,pn,rs) ;
	    shio_printf(pip->efp,"%s: ofile=%s\n",pn,ofn) ;
	}

	return (rs >= 0) ? c : rs ;
}
/* end subroutine (procargs) */


static int prochosts(PROGINFO *pip,PARAMOPT *pop,void *ofp,cchar *lbuf,int llen)
{
	FIELD		fsb ;
	int		rs ;
	int		c = 0 ;
	if ((rs = field_start(&fsb,lbuf,llen)) >= 0) {
	    int		fl ;
	    cchar	*fp ;
	    while ((fl = field_get(&fsb,aterms,&fp)) >= 0) {
	        if (fl > 0) {
	            rs = prochost(pip,pop,ofp,fp,fl) ;
	            c += rs ;
	        }
	        if (fsb.term == '#') break ;
	        if (rs < 0) break ;
	    } /* end while */
	    field_finish(&fsb) ;
	} /* end if (field) */
	return (rs >= 0) ? c : rs ;
}
/* end subroutine (prochosts) */


int prochost(PROGINFO *pip,PARAMOPT *pop,void *ofp,cchar *hp,int hl)
{
	NULSTR		s ;
	int		rs ;
	int		rs1 ;
	int		f_alive = FALSE ;
	cchar		*pn = pip->progname ;
	cchar		*fmt ;
	cchar		*hn ;

	if (pop == NULL) return SR_FAULT ;

#if	CF_DEBUG
	if (DEBUGLEVEL(3))
	    debugprintf("prochost: ent h=%t\n",hp,hl) ;
#endif

	if ((rs = nulstr_start(&s,hp,hl,&hn)) >= 0) {
	    int		f = (pip->verboselevel > 0) ;

	    rs = inetping(hn,pip->to) ;

#if	CF_DEBUG
	if (DEBUGLEVEL(3))
	    debugprintf("prochost: inetping() rs=%d\n",rs) ;
#endif

	    if (rs >= 0) {
	        f_alive = TRUE ;
	        if (f) {
	            fmt = "%s is alive\n" ;
	            shio_printf(ofp,fmt,hn) ;
	        }
	    } else if (rs == SR_HOSTDOWN) {
	        if (f) {
	            fmt = "no answer from %s\n" ;
	            shio_printf(ofp,fmt,hn) ;
	        }
		rs = SR_OK ;
	    } else if (rs == SR_NETUNREACH) {
	        if (f) {
	            fmt = "host network is unreachable (%d)\n" ;
	            shio_printf(ofp,fmt,rs) ;
	        }
		rs = SR_OK ;
	    } else if (rs == SR_HOSTUNREACH) {
	        if (f) {
	            fmt = "host is unreachable! (%d)\n" ;
	            shio_printf(ofp,fmt,rs) ;
	        }
		rs = SR_OK ;
	    } else {
	        if (! pip->f.quiet) {
	            fmt = "%s: inetping error (%d)\n" ;
	            shio_printf(pip->efp,fmt,pn,rs) ;
	        }
	    } /* end if */

	    if (rs >= 0) {
	        LOCINFO	*lip = pip->lip ;
	        rs = locinfo_result(lip,f_alive) ;
	    }

	    rs1 = nulstr_finish(&s) ;
	    if (rs >= 0) rs = rs1 ;
	} /* end if (nulstr) */

#if	CF_DEBUG
	if (DEBUGLEVEL(3))
	    debugprintf("prochost: ret rs=%d f_alive=%u\n",rs,f_alive) ;
#endif

	return (rs >= 0) ? f_alive : rs ;
}
/* end subroutine (prochost) */


static int locinfo_start(LOCINFO *lip,PROGINFO *pip)
{
	int		rs = SR_OK ;

	memset(lip,0,sizeof(LOCINFO)) ;
	lip->pip = pip ;

	return rs ;
}
/* end subroutine (locinfo_start) */


static int locinfo_finish(LOCINFO *lip)
{
	int		rs = SR_OK ;
	int		rs1 ;

	if (lip == NULL) return SR_FAULT ;

	if (lip->open.stores) {
	    lip->open.stores = FALSE ;
	    rs1 = vecstr_finish(&lip->stores) ;
	    if (rs >= 0) rs = rs1 ;
	}

	return rs ;
}
/* end subroutine (locinfo_finish) */



.SM "REASONS MOVING US TO SET DIVERSITY OF SENSES IN THE MARGIN,"
.SM "WHERE THERE IS GREAT PROBABILITY FOR EACH"

Some peradventure would have no variety of senses to be set in the
margin, lest the authority of the Scriptures for deciding of
controversies by that show of uncertainty, should somewhat be shaken.
But we hold their judgment not to be sound in this point.  
For though,
"whatsoever things are necessary are manifest," as S.\ Chrysostom saith,
[S.\ Chrysost. in II. Thess. cap. 2.] and as S.\ Augustine, "In those
things that are plainly set down in the Scriptures, all such matters
are found that concern Faith, Hope, 
and Charity." [S.\ Aug. 2. de doctr.  Christ. cap. 9.]\ \0
Yet for all that it cannot be dissembled,
that partly to exercise and whet our wits, partly to wean the curious
from the loathing of them for their everywhere plainness, partly also
to stir up our devotion to crave the assistance of God's spirit by
prayer, and lastly, that we might be forward to seek aid of our
brethren by conference, and never scorn those that be not in all
respects so complete as they should be, being to seek in many things
ourselves, it hath pleased God in his divine providence, here and there
to scatter words and sentences of that difficulty and doubtfulness, not
in doctrinal points that concern salvation, (for in such it hath been
vouched that the Scriptures are plain) but in matters of less moment,
that fearfulness would better beseem us than confidence, and if we will
resolve upon modesty with S.\ Augustine, (though not in this same case
altogether, yet upon the same ground) Melius est debitare de occultis,
quam litigare de incertis, [S.\ Aug li\(hy S.\ de Genes. ad liter. cap. 5.]
"it is better to make doubt of those things which are secret, than to
strive about those things that are uncertain."  
There be many words in
the Scriptures, which be never found there but once, (having neither
brother or neighbor, as the Hebrews speak) so that we cannot be holpen
by conference of places.  
Again, there be many rare names of certain
birds, beasts and precious stones, etc\(hy concerning the Hebrews
themselves are so divided among themselves for judgment, that they may
seem to have defined this or that, rather because they would say
something, than because they were sure of that which they said, 
as S.\ Jerome somewhere saith of the Septuagint.  
Now in such a case, doth not
a margin do well to admonish the Reader to seek further, and not to
conclude or dogmatize upon this or that peremptorily?  
For as it is a
fault of incredulity, to doubt of those things that are evident: so to
determine of such things as the Spirit of God hath left (even in the
judgment of the judicious) questionable, can be no less than
presumption.  
Therefore as S.\ Augustine saith, that variety of
Translations is profitable for the finding out of the sense of the
Scriptures: [S.\ Aug. 2. De doctr. Christian. cap. 14.] so diversity of
signification and sense in the margin, where the text is no so clear,
must needs do good, yea, is necessary, as we are persuaded.  
We know
that Sixtus Quintus expressly forbiddeth, that any variety of readings
of their vulgar edition, should be put in 
the margin, [Sixtus 5. praef. Bibliae.] (which though it be 
not altogether the same thing to
that we have in hand, yet it looketh that way) but we think he hath not
all of his own side his favorers, for this conceit.  
They that are
wise, had rather have their judgments at liberty in differences of
readings, than to be captivated to one, when it may be the other.  
If
they were sure that their high Priest had all laws shut up in his
breast, as Paul the Second bragged, [Plat. in Paulo secundo.] and that
he were as free from error by special privilege, as the Dictators of
Rome were made by law inviolable, it were another matter; then his word
were an Oracle, his opinion a decision. 
But the eyes of the world are
now open, God be thanked, and have been a great while, they find that
he is subject to the same affections and infirmities that others be,
that his skin is penetrable, and therefore so much as he proveth, not
as much as he claimeth, they grant and embrace.


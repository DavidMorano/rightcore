.SM "THE BEST THINGS HAVE BEEN CULMINATED"

Zeal to promote the common good, whether it be by devising anything
ourselves, or revising that which hath been laboured by others,
deserveth certainly much respect and esteem, but yet findeth but cold
entertainment in the world.  
It is welcomed with suspicion instead of
love, and with emulation instead of thanks: and if there be any hole
left for cavil to enter, (and cavil, if it do not find a hole, will
make one) it is sure to be misconstrued, and in danger to be
condemned.  
This will easily be granted by as many as know story, or
have any experience.  
For, was there ever any-projected, that savoured
any way of newness or renewing, but the same endured many a storm of
gainsaying, or opposition?  
A man would think that Civility, wholesome
Laws, learning and eloquence, Synods, and Church-maintenance, (that we
speak of no more things of this kind) should be as safe as a Sanctuary,
and out of shot, as they say, that no man would lift up the heel, no,
nor dog move his tongue against the motioners of them. 
For by the
first, we are distinguished from brute beasts lead with sensuality; By
the second, we are bridled and restrained from outrageous behaviour,
and from doing of injuries, whether by fraud or by violence; By the
third, we are enabled to inform and reform others, by the light and
feeling that we have attained unto ourselves; Briefly, by the fourth
being brought together to a parley face to face, we sooner compose our
differences than by writings which are endless; And lastly, that the
Church be sufficiently provided for, is so agreeable to good reason and
conscience, that those mothers are holden to be less cruel, that kill
their children as soon as they are born, than those nursing fathers and
mothers (wheresoever they be) that withdraw from them who hang upon
their breasts (and upon whose breasts again themselves do hang to
receive the Spiritual and sincere milk of the word) livelihood and
support fit for their estates.  
Thus it is apparent, that these things
which we speak of, are of most necessary use, and therefore, that none,
either without absurdity can speak against them, or without note of
wickedness can spurn against them.

Yet for all that, the learned know that certain worthy men [Anacharsis
with others] have been brought to untimely death for none other fault,
but for seeking to reduce their Countrymen to god order and discipline;
and that in some Commonwealths (e.g. Locri) it was made a capital
crime, once to motion the making of a new Law for the abrogating of an
old, though the same were most pernicious; And that certain [Cato the
elder], which would be counted pillars of the State, and patterns of
Virtue and Prudence, could not be brought for a long time to give way
to good Letters and refined speech, but bare themselves as averse from
them, as from rocks or boxes of poison; And fourthly, that he was no
babe, but a great clerk [Gregory the Divine], that gave forth (and in
writing to remain to posterity) in passion peradventure, but yet he
gave forth, that he had not seen any profit to come by any Synod, or
meeting of the Clergy, but rather the contrary; And lastly, against
Church-maintenance and allowance, in such sort, as the Ambassadors and
messengers of the great King of Kings should be furnished, it is not
unknown what a fiction or fable (so it is esteemed, and for no better
by the reporter himself [Nauclerus], though superstitious) was devised;
Namely, that at such a time as the professors and teachers of
Christianity in the Church of Rome, then a true Church, were liberally
endowed, a voice forsooth was heard from heaven, saying: Now is poison
poured down into the Church, etc\(hy.
Thus not only as oft as we speak, as
one saith, but also as oft as we do anything of note or consequence, we
subject ourselves to everyone's censure, and happy is he that is least
tossed upon tongues; for utterly to escape the snatch of them it is
impossible.  
If any man conceit, that this is the lot and portion of
the meaner sort only, and that Princes are privileged by their high
estate, he is deceived.  
"As the sword devoureth as well one as the
other," as it is in Samuel [II\ Samuel\ 11:25], nay as the great Commander
charged his soldiers in a certain battle, to strike at no part of the
enemy, but at the face; And as the King of Syria commanded his chief
Captains to "fight neither with small nor great, save only against the
King of Israel:" [I\ Kings 22:31] so it is too true, that Envy striketh
most spitefully at the fairest, and at the chiefest.  
David was a
worthy Prince, and no man to be compared to him for his first deeds,
and yet for as worthy as act as ever he did (even for bringing back the
Ark of God in solemnity) he was scorned and scoffed at by his own wife
[II\ Samuel\ 6:16].  
Solomon was greater than David, though not in virtue,
yet in power: and by his power and wisdom he built a Temple to the
Lord, such a one as was the glory of the land of Israel, and the wonder
of the whole world.  
But was that his magnificence liked of by all?  
We
doubt it.  
Otherwise, why do they lay it in his son's dish, and call
unto him for easing the burden, "Make," say they, "the grievous
servitude of thy father, and his sore yoke, lighter?" [I\ Kings\ 12:4]\ \0
Belike he had charged them with some levies, and troubled them with
some carriages; Hereupon they raise up a tragedy, and wish in their
heart the Temple had never been built.  
So hard a thing it is to please
all, even when we please God best, and do seek to approve ourselves to
every ones conscience.

If we will descend to later times, we shall find many the like examples
of such kind, or rather unkind acceptance.  
The first Roman Emperor
[C. Caesar. Plutarch] did never do a more pleasing deed to the
learned, nor more profitable to posterity, for conserving the record of
times in true supputation; than when he corrected the Calendar, and
ordered the year according to the course of the Sun; and yet this was
imputed to him for novelty, and arrogance, and procured to him great
obloguy.  
So the first Christened Emperor [Constantine] (at the
least-wise that openly professed the faith himself, and allowed others
to do the like) for strengthening the Empire at his great charges, and
providing for the Church, as he did, got for his labour the name
Pupillus, as who would say, a wasteful Prince, that had need of a
Guardian or overseer [Aurel, Victor].  
So the best Christened Emperor [Theodosius], 
for the love that he bare unto peace, thereby to enrich
both himself and his subjects, and because he did not see war but find
it, was judged to be no man at arms [Zosimus], (though indeed he
excelled in feats of chivalry, and showed so much when he was provoked)
and condemned for giving himself to his ease, and to his pleasure.  
To
be short, the most learned Emperor of former times [Justinian], (at the
least, the greatest politician) what thanks had he for cutting off the
superfluities of the laws, and digesting them into some order and
method?  
This, that he had been blotted by some to be an Epitomist,
that is, one that extinguishes worthy whole volumes, to bring his
abridgments into request.  
This is the measure that hath been rendered
to excellent Princes in former times, even, Cum bene facerent, male
audire, For their good deeds to be evil spoken of.  
Neither is there
any likelihood, that envy and malignity died, and were buried with the
ancient.  
No, no, the reproof of Moses taketh hold of most ages; "You
are risen up in your fathers' stead, 
and increase of sinful men." [Numbers\ 32:14]\ \0
"What is that that hath been done? that which shall be done;
and there is no new thing under the Sun," 
saith the wiseman: [Ecclesiastes\ 1:9]
and S.\ Stephen, "As your fathers did, so do you." [Acts\ 7:51]


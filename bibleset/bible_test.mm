.SM "THE PRAISE OF THE HOLY SCRIPTURES"

But now what piety without truth? what truth (what saving truth)
without the word of God?  
What word of God (whereof we may be sure)
without the Scripture?  
The Scriptures we are commanded to search [John\ 5:39, Isaiah\ 8:20].  
They are commended that searched and studied 
them [Acts\ 17:11, and Acts\ 8:28-29].  
They are reproved that were unskillful in
them, or slow to believe them [Matthew\ 22:29, Luke\ 24:25].  
They can make
us wise unto salvation [II\ Timothy\ 3:15].  
If we be ignorant, they will
instruct us; if out of the way, they will bring us home; if out of
order, they will reform us; if in heaviness, comfort us; if dull,
quicken us; if cold, inflame us.  
Tolle, lege; Tolle, lege, Take up and
read, take up and read the Scriptures [S.\ August. confess. lib 8 cap
12], (for unto them was the direction) it was said unto S.\ Augustine by
a supernatural voice.  
"Whatsoever is in the Scriptures, believe me,"
saith the same S.\ Augustine, "is high and divine; there is verily
truth, and a doctrine most fit for the refreshing of men's minds, and
truly so tempered, that everyone may draw from thence that which is
sufficient for him, if he come to draw with a devout and pious mind, as
true Religion requireth." [S.\ August. de utilitcredendi cap. 6]\ \0
Thus S.\ Augustine. and S.\ Jerome: "Ama scripturas, et amabit te sapientia
etc\(hy" [S.\ Jerome. ad Demetriad]\ \0
Love the Scriptures, and wisdom will
love thee.  
And S.\ Cyril against Julian; "Even boys that are bred up in
the Scriptures, become most religious, 
etc\(hy" [S.\ Cyril. 7 contra Iulianum]\ \0
But what mention we three or four uses of the Scripture,
whereas whatsoever is to be believed or practiced, or hoped for, is
contained in them? or three or four sentences of the Fathers, since
whosoever is worthy the name of a Father, from Christ's time downward,
hath likewise written not only of the riches, but also of the
perfection of the Scripture?  
"I adore the fulness of the Scripture,"
saith Tertullian against Hermogenes [Tertul. advers. Hermo.].
And
again, to Apelles an heretic of the like stamp, he saith; "I do not
admit that which thou bringest in (or concludest) of thine own (head or
store, de tuo) without Scripture." [Tertul. de carne Christi.]\ \0
So
Saint Justin Martyr before him; "We must know by all means," saith he,
"that it is not lawful (or possible) to learn (anything) of God or of
right piety, save only out of the Prophets, who teach us by divine
inspiration." 
So Saint Basil after Tertullian, "It is a manifest
falling way from the Faith, and a fault of presumption, either to
reject any of those things that are written, or to bring in (upon the
head of them) any of those things that are not written.  
We omit to
cite to the same effect, S.\ Cyril\ B.\ of Jerusalem in his 4::Cataches.,
Saint Jerome against Helvidius, Saint Augustine in his 3::book against
the letters of Petilian, and in very many other places of his works.
Also we forebear to descend to later Fathers, because we will not weary
the reader.  
The Scriptures then being acknowledged to be so full and
so perfect, how can we excuse ourselves of negligence, if we do not
study them, of curiosity, if we be not content with them?  
Men talk
much of [an olive bow wrapped about with wood, whereupon did hang figs,
and bread, honey in a pot, and oil], how many sweet and goodly things
it had hanging on it; of the Philosopher's stone, that it turned copper
into gold; of Cornucopia, that it had all things necessary for food in
it, of Panaces the herb, that it was good for diseases, of Catholicon
the drug, that it is instead of all purges; of Vulcan's armor, that it
was an armor of proof against all thrusts, and all blows, etc\(hy.
Well,
that which they falsely or vainly attributed to these things for bodily
god, we may justly and with full measure ascribe unto the Scripture,
for spiritual.  
It is not only an armor, but also a whole armory of
weapons, both offensive and defensive; whereby we may save ourselves
and put the enemy to flight.  
It is not an herb, but a tree, or rather
a whole paradise of trees of life, which bring forth fruit every month,
and the fruit thereof is for meat, and the leaves for medicine.  
It is
not a pot of Manna, or a cruse of oil, which were for memory only, or
for a meal's meat or two, but as it were a shower of heavenly bread
sufficient for a whole host, be it never so great; and as it were a
whole cellar full of oil vessels; whereby all our necessities may be
provided for, and our debts discharged.  
In a word, it is a Panary of
wholesome food, against fenowed traditions; a Physician's shop (Saint
Basil called it) [S.\ Basil in Psal. primum.] of preservatives against
poisoned heresies; a Pandect of profitable laws, against rebellious
spirits; a treasury of most costly jewels, against beggarly rudiments;
finally a fountain of most pure water springing up unto everlasting
life.  
And what marvel? 
The original thereof being from heaven, not
from earth; the author being God, not man; the inditer, the holy
spirit, not the wit of the Apostles or Prophets; the Penmen such as
were sanctified from the womb, and endued with a principal portion of
God's spirit; the matter, verity, piety, purity, uprightness; the form,
God's word, God's testimony, God's oracles, the word of truth, the word
of salvation, etc\(hy; the effects, light of understanding, stableness of
persuasion, repentance from dead works, newness of life, holiness,
peace, joy in the holy Ghost; lastly, the end and reward of the study
thereof, fellowship with the Saints, participation of the heavenly
nature, fruition of an inheritance immortal, undefiled, and that never
shall fade away: Happy is the man that delighted in the Scripture, and
thrice happy that meditateth in it day and night.

